#include<stdio.h>  // Preprocessor Section

int main() // Main Function
{
	int x = 66;
	char p='p';
	float y=73.8;
	int z = -55;

	printf("\n Integer and character value of x :%d --> %C",x,x);
	printf("\n Integer and character value of p :%d --> %C",p,p);
	printf("\n Integer and Hex value of x :%d --> %x",x,x);
	printf("\n Integer and octal value of x :%f --> %o",x,x);
	printf("\n Integer and float value of y :%f --> %d",y,y);
	printf("\n Integer and unsigned value of z :%d --> %u",z,z);

}
